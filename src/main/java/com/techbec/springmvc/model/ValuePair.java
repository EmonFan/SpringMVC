package com.techbec.springmvc.model;

public class ValuePair {
    private long 	time;
    private Double  value;
    
	public long getTime() {
		return time;
	}
	public void setTime(long time) {
		this.time = time;
	}
	public Double getValue() {
		return value;
	}
	public void setValue(Double value) {
		this.value = value;
	}

}
